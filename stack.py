#simple stack implementation using python
stack=[]

#push
stack.append('p')
stack.append('q')
stack.append('r')
stack.append('s')

print(stack)

#pop

print(stack.pop())

print("Stack after elements are popped:")
print(stack)

print(stack.pop())

print("Stack after elements are popped:")
print(stack)


stack.pop()
stack.pop()

print("Stack after elements are popped:")
print(stack)

